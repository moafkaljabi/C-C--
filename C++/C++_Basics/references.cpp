// references  // pointers

#include <iostream>
using namespace std;

int main()
{
    // references

    string food = "Pizza";
    string &meal = food;

    cout << food << endl;
    cout << meal << endl;

    cout << &food << endl;
    cout << &meal << endl;

    // pointers
    string myName = "Mwafak";
    string *namePtr = &myName;

    // value of a string
    cout << myName << endl;

    // adress of pointer using the value itself.
    cout << &myName << endl;

    // adress of pointer.
    cout << namePtr << endl;

    // to dereference ; get the value in a certain memory address in a pointer.
    cout << *namePtr << endl;

    // changing pointer value...

    string myfood = "Pizza";
    string *newPointer = &myfood;

    // print value of our string , that is in the pointer newPointer.
    cout << myfood << endl;

    // print it using the pointer holding it.
    cout << *newPointer << endl;

    // print location of string using the pointer.

    cout << newPointer << endl;

    // change the value of the string using the pointer.
    *newPointer = "Burger";

    // print the new value of string.
    cout << myfood << endl;

    // print the  new value of string using the pointer.
    cout << *newPointer << endl;

    return 0;
}
